import pandas as pd

df = pd.read_csv('/content/Salary Data.csv')

"""### Handling Missing and Blank Values"""

print('Missing values before imputation:')
print(df.isnull().sum())

# Check for blank strings in object columns
for col in df.select_dtypes(include='object').columns:
    if (df[col] == '').any():
        print(f"Column '{col}' contains blank strings.")

"""Based on the check, I will fill numerical columns with their mean and categorical columns with 'Unknown'."""

# Fill numerical columns with their mean
for col in df.select_dtypes(include=['float64', 'int64']).columns:
    if df[col].isnull().any():
        df[col] = df[col].fillna(df[col].mean())

# Fill categorical columns (object type) with 'Unknown' or mode, and also replace blank strings
for col in df.select_dtypes(include='object').columns:
    # Fill NaN values
    if df[col].isnull().any():
        df[col] = df[col].fillna('Unknown')
    # Replace blank strings with 'Unknown'
    if (df[col] == '').any():
        df[col] = df[col].replace('', 'Unknown')

print('\nMissing values after imputation:')
print(df.isnull().sum())

print('\nDataFrame info after imputation:')
df.info()

display(df.head())

from sklearn.preprocessing import LabelEncoder

label_encoder = LabelEncoder()
for column in ['Gender','Education Level','Job Title']:
    df[column] = label_encoder.fit_transform(df[column])
print(df.head())

from sklearn.model_selection import train_test_split

x = df.drop('Salary',axis=1)
y = df['Salary']
x_train,x_test,y_train,y_test = train_test_split(x,y,test_size=0.2,random_state=42)

from sklearn.linear_model import LinearRegression
from sklearn.tree import DecisionTreeRegressor
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_squared_error, r2_score

# Initialize models
linear_model = LinearRegression()
decision_tree_model = DecisionTreeRegressor(random_state=42)
random_forest_model = RandomForestRegressor(random_state=42)

models = {
    'Linear Regression': linear_model,
    'Decision Tree Regressor': decision_tree_model,
    'Random Forest Regressor': random_forest_model
}

# Train and evaluate each model
results = {}

for name, model in models.items():
    print(f"\nTraining {name}...")
    model.fit(x_train, y_train)
    y_pred = model.predict(x_test)

    mse = mean_squared_error(y_test, y_pred)
    r2 = r2_score(y_test, y_pred)

    results[name] = {'MSE': mse, 'R2': r2}

    print(f"{name} - Mean Squared Error: {mse:.2f}")
    print(f"{name} - R-squared: {r2:.2f}")

print("\n--- Model Evaluation Summary ---")
for name, metrics in results.items():
    print(f"{name}:")
    print(f"  MSE: {metrics['MSE']:.2f}")
    print(f"  R2: {metrics['R2']:.2f}")

import matplotlib.pyplot as plt
import numpy as np

# Extract model names and performance metrics
model_names = list(results.keys())
mse_scores = [metrics['MSE'] for metrics in results.values()]
r2_scores = [metrics['R2'] for metrics in results.values()]

x = np.arange(len(model_names))  # the label locations
width = 0.35  # the width of the bars

fig, ax = plt.subplots(1, 2, figsize=(14, 6))

# Plot MSE
rects1 = ax[0].bar(x, mse_scores, width, label='MSE', color='skyblue')
ax[0].set_ylabel('Mean Squared Error')
ax[0].set_title('Model Performance: Mean Squared Error')
ax[0].set_xticks(x)
ax[0].set_xticklabels(model_names, rotation=45, ha='right')
ax[0].legend()
ax[0].bar_label(rects1, padding=3, fmt='%.2f')

# Plot R-squared
rects2 = ax[1].bar(x, r2_scores, width, label='R-squared', color='lightcoral')
ax[1].set_ylabel('R-squared Score')
ax[1].set_title('Model Performance: R-squared Score')
ax[1].set_xticks(x)
ax[1].set_xticklabels(model_names, rotation=45, ha='right')
ax[1].legend()
ax[1].bar_label(rects2, padding=3, fmt='%.2f')

plt.tight_layout()
plt.show()

import pickle

# Assuming random_forest_model is the best performing model based on previous evaluation
best_model = random_forest_model

# Save the model to a .pkl file
filename = 'random_forest_model.pkl'
pickle.dump(best_model, open(filename, 'wb'))

print(f"Best model saved as {filename}")

